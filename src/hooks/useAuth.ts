
import { useState, useEffect } from 'react';
import { supabase } from '@/integrations/supabase/client';
import { toast } from '@/hooks/use-toast';
import { User } from '@supabase/supabase-js';

export const useAuth = () => {
  const [user, setUser] = useState<User | null>(null);
  const [isLoading, setIsLoading] = useState(true);
  const [isAdmin, setIsAdmin] = useState(false);

  // Admin emails - add your email here
  const ADMIN_EMAILS = ["admin@pidrop.dev"];
  
  // Check for test user session
  const getTestUserSession = () => {
    try {
      const testSession = localStorage.getItem('test_user_session');
      if (testSession) {
        const parsedSession = JSON.parse(testSession);
        if (parsedSession.session.expires_at > Date.now()) {
          return parsedSession;
        } else {
          localStorage.removeItem('test_user_session');
        }
      }
    } catch (error) {
      console.error('Error parsing test session:', error);
      localStorage.removeItem('test_user_session');
    }
    return null;
  };
  
  useEffect(() => {
    // Security improvement: Add authorization header and token validation
    const checkAuthHeaders = async () => {
      // Check for test session first
      const testSession = getTestUserSession();
      if (testSession) {
        console.log("Using test session");
        return true;
      }
      
      // Check if auth state includes valid tokens
      const { data } = await supabase.auth.getSession();
      
      if (data.session?.access_token) {
        // Validate token expiry
        if (new Date(data.session.expires_at * 1000) < new Date()) {
          console.warn("Access token has expired");
          await signOut(); // Force sign out if token expired
          return false;
        }
        console.log("Valid access token present");
        return true;
      }
      return false;
    };
    
    // Set up auth state change listener with improved security
    const { data: { subscription: authSubscription } } = supabase.auth.onAuthStateChange(
      async (event, session) => {
        console.log("Auth state change:", event, session?.user?.id);
        
        // Check for test session first
        const testSession = getTestUserSession();
        if (testSession) {
          setUser(testSession.user);
          setIsAdmin(testSession.user.username === 'admin' || ADMIN_EMAILS.includes(testSession.user.email));
          setIsLoading(false);
          return;
        }
        
        const currentUser = session?.user ?? null;
        setUser(currentUser);
        
        // Security enhancement: Strict admin validation with email verification
        if (currentUser && currentUser.email && currentUser.email_confirmed_at) {
          const userIsAdmin = ADMIN_EMAILS.includes(currentUser.email);
          setIsAdmin(userIsAdmin);
          
          if (userIsAdmin) {
            console.log("User has admin privileges");
          }
        } else {
          setIsAdmin(false);
        }
        
        setIsLoading(false);
      }
    );

    // Check current session with improved security
    const initAuth = async () => {
      try {
        // Check for test session first
        const testSession = getTestUserSession();
        if (testSession) {
          console.log("Found test session, using test user");
          setUser(testSession.user);
          setIsAdmin(testSession.user.username === 'admin' || ADMIN_EMAILS.includes(testSession.user.email));
          setIsLoading(false);
          return;
        }
        
        const { data: { session } } = await supabase.auth.getSession();
        console.log("Current session:", session?.user?.id);
        const currentUser = session?.user ?? null;
        setUser(currentUser);
        
        // Security enhancement: Strict admin validation with email verification
        if (currentUser && currentUser.email && currentUser.email_confirmed_at) {
          const userIsAdmin = ADMIN_EMAILS.includes(currentUser.email);
          setIsAdmin(userIsAdmin);
          
          if (userIsAdmin) {
            console.log("User has admin privileges");
          }
        }
        
        // Run security check
        await checkAuthHeaders();
        setIsLoading(false);
      } catch (error) {
        console.error("Error initializing auth:", error);
        setUser(null);
        setIsAdmin(false);
        setIsLoading(false);
      }
    };

    initAuth();

    return () => {
      authSubscription.unsubscribe();
    };
  }, []);

  const signOut = async () => {
    try {
      // Clear test session first
      localStorage.removeItem('test_user_session');
      
      await supabase.auth.signOut();
      
      // Clear all local storage items related to authentication
      // Security improvement: More comprehensive cleanup
      localStorage.removeItem('userToken');
      localStorage.removeItem('username');
      localStorage.removeItem('userEmail');
      localStorage.removeItem('userPlan');
      localStorage.removeItem('piUsername');
      localStorage.removeItem('piUserId');
      localStorage.removeItem('piAccessToken');
      localStorage.removeItem('subscriptionEnd');
      
      // Clear Supabase storage items
      localStorage.removeItem('supabase.auth.token');
      
      // Clear any session cookies
      document.cookie.split(';').forEach(c => {
        document.cookie = c.replace(/^ +/, '').replace(/=.*/, '=;expires=' + new Date().toUTCString() + ';path=/');
      });
      
      console.log("User signed out successfully");
      
      toast({
        title: "Signed Out",
        description: "You have been successfully signed out",
      });
      
      // Force page reload to clear any in-memory state
      window.location.href = '/';
    } catch (error) {
      console.error("Error signing out:", error);
      toast({
        title: "Error",
        description: "Failed to sign out",
        variant: "destructive",
      });
    }
  };

  return {
    user,
    isLoading,
    isAdmin,
    isLoggedIn: !!user,
    signOut
  };
};
